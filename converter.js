// Func: Gn$ to CHF
// Gn$ * 0.97
function Gn$ToCHF(Gn$){
    return Gn$ * 0.97;
}

// func: Celsius to Farenheit
// (celsius * 9/5) + 32
function celsiusToFarenheit(celsius){
    (celsius * 9/5) + 32
}

// Func: Liters to Gallons
// liters / 3.785
function litersToGallons(liters) {
    return liters / 3.785;
}


